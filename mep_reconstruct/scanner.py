# ── CSV Reading Helpers ──────────────────────────────────────────────────────

def safe_read_csv(filepath, max_rows=None):
    """Read CSV with encoding detection, return list of dicts."""
    for enc in ["utf-8-sig", "utf-8", "latin-1", "utf-16"]:
        try:
            with open(filepath, "r", encoding=enc, errors="replace") as f:
                # Detect delimiter
                sample = f.read(2048)
                f.seek(0)
                delim = "\t" if sample.count("\t") > sample.count(",") else ","
                reader = csv.DictReader(f, delimiter=delim)
                rows = []
                for i, row in enumerate(reader):
                    if max_rows and i >= max_rows:
                        break
                    rows.append(row)
                return rows
        except Exception:
            continue
    return []


def find_csvs(base_path, pattern):
    """Find CSV files matching a name pattern (case-insensitive)."""
    results = []
    pattern_lower = pattern.lower()
    for f in Path(base_path).rglob("*.csv"):
        if pattern_lower in f.name.lower():
            results.append(f)
    return sorted(results)


